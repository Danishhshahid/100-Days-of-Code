"use strict";
//Compound assignment operators:
function operators() {
    let a = 4;
    console.log(`inital value of a is `, a);
    a += 4; // 8
    console.log(`after addition value of a is`, a);
    a -= 2; //2
    console.log(`after subrtaction value of a is`, a);
    a /= 2; //2
    console.log(`after division value of a is`, a);
    a *= 2; //8
    console.log(`after multiplication value of a is`, a);
}
operators();
